#include <stdio.h>
int main () {
  int n;
  scanf("%d", &n);
  for (int i=0; i<n; i++) {
    // n-(i-1)개 공백
    // i+1개 별
    for (int j=0; j<n-(i+1); j++) {
      printf(" ");
    }
    for (int j=0; j<i+1; j++) {
      printf("* ");
    }
    printf("\n");
  }
}